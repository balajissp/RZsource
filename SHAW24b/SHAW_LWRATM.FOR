C
      SUBROUTINE LWRATM (ABOVE,CLOUDS,TA,VAPA)
C
C     THIS SUBROUTINE DETERMINES THE LONG-WAVE RADIATIVE FLUX FROM THE
C     ATMOSPHERE.
C
C***********************************************************************
C
      IMPLICIT DOUBLE PRECISION (A-H,O-Z)
C
      PARAMETER (STEFAN=5.6697D-08,EMATM1=0.261D0,EMATM2=7.77D-04)
C
C**** DETERMINE THE EMISSIVITY OF CLEAR-SKY ATMOSPHERE
c      EMITAT=1.0D0 - EMATM1*EXP(-EMATM2*TA*TA)
C**** DETERMINE THE EMISSIVITY OF CLEAR-SKY ATMOSPHERE BASED ON !New based on Flerchinger, 25b
C     DILLEY & O'BRIEN (1998), QJR MET SOC.
c      AVGTMP = WT*TA + WDT*TADT
      AVGTMP = TA
c      AVGVAP = WT*VAPA + WDT*VAPADT
      AVGVAP = VAPA
C     CONVERT TO VAPOR PRESSURE (kPa)
      AVGVAP = 0.4619d0*AVGVAP*(AVGTMP+273.16d0)
      CLEAR =59.38d0+113.7d0*((AVGTMP+273.16)/273.16d0)**6.0d0 
     >          +96.96d0*SQRT((465.d0*AVGVAP/(AVGTMP+273.16d0))/2.5d0)
      BLACK = STEFAN*(AVGTMP+273.16d0)**4.0d0
C     BACK-CALCULATE EMISSIVITY FROM CLEAR-SKY LONG-WAVE RADIATION
      EMITAT=CLEAR/BLACK
C
C**** ADJUST CLEAR-SKY EMISSIVITY FOR FRACION OF CLOUD COVER
CC    EMITAT=EMITAT + CLOUDS*(1.D0-EMITAT-8.D0/(273.16D0+TA))
CC    USE EQUATION GIVEN CAMPBELL, 1985 (SOIL PHYSICS WITH BASIC)
      EMITAT=(1.D0-0.84D0*CLOUDS)*EMITAT + 0.84D0*CLOUDS
C
C**** NOW CALCULATE THE LONG-WAVE RADIATIVE FLUX
c      ABOVE = EMITAT*STEFAN*(TA+273.16D0)**4.0D0
C**** NOW CALCULATE THE LONG-WAVE RADIATIVE FLUX  !New
      ABOVE = EMITAT*BLACK
C
      RETURN
      END
C***********************************************************************
